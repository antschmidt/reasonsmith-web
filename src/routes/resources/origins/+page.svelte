	<div class="story-content">
		<section class="story-section">
			<h2>The Beginning</h2>
			<p>
				Growing up in a small town in Eastern Iowa, I'd feel the social and conformity pressures
				that come with that kind of place. But small towns also meant you couldn't avoid people you
				disagreed with - at least within a certain range. Everyone went to the same high school, the
				same stores, etc. The cultural range was narrow, but being social animals with the geography
				limiting the people we're able to relate with, forced interaction across whatever
				differences existed in a way that's become increasingly rare.
			</p>
			<p>
				"It's a big world out there" was a saying that floated around - more abstract comfort than
				lived reality. Socially, the world beyond our town didn't feel real, or in any way impactful
				to me. Long-distance phone calls were prohibitively expensive. Pen pals took weeks to
				exchange letters. Meeting people from other countries, outside of a handful of exchange
				students each year, required the kind of money and access I didn't have.
			</p>
			<p>
				Then came IRC chat rooms, and later ICQ. I started talking with someone from China who was
				going to school in Malaysia, trying to get better at English. We'd discuss food, travels,
				culture - just genuine conversations between people curious about each other's lives. Years
				later, I met up with her in London. That abstract saying - "it's a big world out there" -
				had become concrete. The internet made something impossible suddenly ordinary. My world was
				expanding exponentially while the world itself was getting much smaller.
			</p>
			<p>
				This wasn't just about me connecting with individuals across the globe. It was part of two
				parallel revolutions happening simultaneously: one in human connection, and one in access to
				information.
			</p>
		</section>

		<section class="story-section">
			<h2>The Dream</h2>
			<p>
				Those early forums and chat rooms weren't utopias - people could absolutely be assholes. But
				bad behavior wasn't rewarded beyond the moment. There was no financial incentive to be
				divisive or inflammatory. No algorithm amplifying outrage because it kept people engaged. If
				someone was consistently terrible, communities would ignore them or they'd get banned, and
				that was that. The infrastructure was neutral.
			</p>
			<p>
				People had to learn how to use the tools, had to want to be there. The friction meant that
				those who showed up generally wanted actual conversation, or at least weren't being paid to
				ruin it. It was unmonetized, self-selected, human-scale. We believed that if we just removed
				the barriers - distance, gatekeepers, cost - and didn't create perverse incentives, human
				curiosity could flourish.
			</p>
			<p>
				At the same time, another group of pioneers was fighting a different battle for access.
				Academic knowledge sat locked behind institutional paywalls, with universities paying tens
				of thousands of dollars per year for journal subscriptions. Publicly funded research
				remained inaccessible to the public who funded it.
			</p>
			<p>
				Then came PLOS - scientists creating open-access journals because they believed knowledge
				should be a public good, not a commodity. Aaron Swartz and the guerrilla open access
				movement argued that "Information is power. But like all power, there are those who want to
				keep it for themselves." The GPL and Linux proved that collaborative, open models could not
				just compete with proprietary ones, but surpass them. Wikipedia showed that human knowledge
				could be a commons, maintained by everyone, available to anyone.
			</p>
			<p>
				The vision was unified: remove gatekeepers, create commons-based peer production, trust that
				good faith collaboration and open access would naturally elevate quality. Information wanted
				to be free; expertise wanted to be shared. And people were willing to pay real costs for
				this vision - Aaron Swartz faced prosecution and ultimately took his own life. Scientists
				pushed against institutional skepticism to publish open access. Developers worked for free
				to build Linux. They believed it mattered that much.
			</p>
		</section>

		<section class="story-section">
			<h2>The Fall</h2>
			<p>
				Let me be clear: these technologies are genuinely remarkable. AI can now translate languages
				in real-time, analyze vast datasets, facilitate connections at unprecedented scale. We have
				access to more information than any generation in human history. The capability isn't the
				problem.
			</p>
			<p>The problem is what we're optimizing for.</p>
			<p>
				The advertising model discovered something: outrage keeps people scrolling more than
				understanding does. Tribalism generates more engagement than nuance. Algorithmic feeds
				learned to serve us content that triggers emotional responses, not content that advances our
				understanding. The metric became "engagement time" rather than "truth-seeking" or "clarity."
			</p>
			<p>
				What we gained in reach, we lost in depth. Those IRC conversations I had as a teenager were
				small-scale but genuine. Today's "global connection" is often algorithmically curated to
				maximize conflict, optimized for performance over conversation, parasocial rather than
				mutual.
			</p>
			<p>
				Now AI is amplifying the problem. When you can generate infinite content at near-zero cost,
				the current incentive structure says: whoever floods the zone fastest wins. The same
				technology that could elevate discourse instead optimizes for spam. We're in a race to the
				bottom where volume matters more than reasoning.
			</p>
			<p>
				I had my own moment of reckoning with this a few years ago. I had an idea about possible
				solutions to our unhoused population and was excitedly explaining it to a friend who was
				getting her master's in public policy. She listened patiently, then said: "That's not how
				cities work. That's not how city funding works."
			</p>
			<p>
				It was humbling. I thought I understood a complex system because I'd read some articles and
				had strong intuitions. But intuition isn't understanding, and access to unlimited
				information doesn't automatically give you clarity. I realized I had little to no true grasp
				of the subject - the budget structures, the political incentives, the constraints cities
				operate under, the research on what actually works.
			</p>
			<p>
				And here's the thing: our current information ecosystem doesn't help bridge that gap. We got
				the access those pioneers fought for - sort of. But we lost the quality signals. Information
				became free but also became weaponized. The openness that empowered also enabled
				misinformation. We have more information than ever before and less clarity about what's
				actually true or who actually understands the problems we face.
			</p>
		</section>

		<section class="story-section">
			<h2>The Path Forward</h2>
			<p>We don't need new technology. We need to fix the incentive structure.</p>
			<p>
				What if we measured what actually matters? Not how many people clicked, but whether ideas
				are supported by evidence. Not how long someone stayed on a page, but whether the
				conversation advanced understanding. Not whether people agreed, but whether they engaged
				substantively with reasoning.
			</p>
			<p>
				This is what ReasonSmith aims to do. A simple response like "Amen!" scores low not because
				agreement is bad, but because it doesn't advance discourse - it provides no logical
				argument, no evidence, no substantive contribution to understanding. Evidence and logical
				reasoning score well because that's what actually serves collaborative truth-seeking.
			</p>
			<p>
				We're inverting the incentive structure. Quality of reasoning over volume of content.
				Evidence over assertion. Clarity over virality. Collaboration over performance.
			</p>
			<p>
				But here's the reality: quality research and reasoning take time - time most people don't
				have. I could see myself funding local researchers to investigate our local problems with
				the kind of depth my friend had about public policy. I could see funding aspiring
				journalists to research national and global issues with actual rigor. We're exploring models
				for funding researchers who demonstrate consistent evidence-based reasoning and depth of
				subject matter expertise.
			</p>
			<p>
				But we're not building this in isolation. We're figuring out how a funding model should
				work, what safeguards are needed, and what types of research our community values most as a
				discussion on ReasonSmith itself - using the platform to shape the platform.
			</p>
			<p>
				We're also making it easy to move from digital discussion to in-person meetups. The internet
				connected us globally, but we lost the local. Those IRC friendships became real when I
				eventually traveled and stayed with people I'd met online. We're building tools that use
				online infrastructure to facilitate offline relationships - using technology to bring people
				together, not keep them scrolling.
			</p>
			<p>
				We're standing on the shoulders of those earlier pioneers. PLOS showed that open access
				matters, but so does quality control. GPL and Linux showed that collaborative models work
				when they have the right structure. Aaron Swartz reminded us that access alone isn't enough
				- we need both accessibility and quality. ReasonSmith is trying to synthesize these
				insights: the openness of Wikipedia, the quality standards of peer review, the collaborative
				model of Linux, the accessibility Swartz fought for - applied not just to information, but
				to reasoning itself.
			</p>
			<p>
				This is about reclaiming the original dream with the wisdom of what we've learned. Those
				early internet pioneers believed that removing distance and gatekeepers would let human
				curiosity and good faith flourish. They were right about the potential and naive about what
				structures would be needed to sustain it. We're not trying to invent something new - we're
				trying to recover what we lost when we let the ad model hijack the incentive structure of
				human connection and knowledge-sharing, but with better safeguards and measures.
			</p>
		</section>

		<section class="story-section story-cta">
			<h2>Join Us</h2>
			<p>
				That kid in Eastern Iowa discovering the world through a computer screen experienced
				something genuinely magical - and real. ReasonSmith aims to make that wonder accessible
				again, but wiser about what structures enable it versus what destroys it.
			</p>
			<p>
				This is the beginning, not a finished product. We're building infrastructure for the kind of
				discourse those pioneers believed was possible: evidence-based, good faith, collaborative
				truth-seeking. If you believe that matters, we'd love to have you help us build it.
			</p>
